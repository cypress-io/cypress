<template>
  <div
    :class="{
      'max-w-3xl mx-auto border-1 border-gray-100 rounded m-10 flex flex-col': !noContainer
    }"
  >
    <div class="flex-grow">
      <slot :backFn="backFn" />
    </div>

    <ButtonBar
      v-if="!noContainer"
      :next-fn="nextFn"
      :can-navigate-forward="canNavigateForward"
      :back-fn="backFn"
      :alt-fn="altFn"
      :next="nextLabel"
      :show-next="showNext"
      :back="backLabel"
      :alt="alt"
    >
      <slot name="button-bar" />
    </ButtonBar>
  </div>
</template>

<script lang="ts" setup>
import ButtonBar from './ButtonBar.vue'
import { computed } from 'vue'
import { useMutation } from '@urql/vue'
import { gql } from '@urql/core'
import { WizardLayoutNavigateDocument } from '../generated/graphql'
import { useI18n } from '@cy/i18n'

gql`
fragment WizardLayout on Wizard {
  title
  description
  step
  canNavigateForward
}
`

gql`
mutation WizardLayoutNavigate($direction: WizardNavigateDirection!) {
  wizardNavigate(direction: $direction) {
    ...WizardLayout
  }
}
`

const { t } = useI18n()

const props = withDefaults(
  defineProps<{
    next?: string
    back?: string
    alt?: string
    showNext?: boolean
    canNavigateForward?: boolean
    noContainer?: boolean
    altFn?:(val: boolean) => void
    nextFn?: (...args: unknown[]) => any,
  }>(), {
    next: undefined,
    showNext: true,
    back: undefined,
    alt: undefined,
    canNavigateForward: undefined,
    noContainer: undefined,
    altFn: undefined,
    nextFn: undefined,
  },
)

const nextLabel = computed(() => props.next || t('setupPage.step.next'))
const backLabel = computed(() => props.back || t('setupPage.step.back'))

const navigate = useMutation(WizardLayoutNavigateDocument)

async function nextFn () {
  await props.nextFn?.()
  navigate.executeMutation({ direction: 'forward' })
}

function backFn () {
  navigate.executeMutation({ direction: 'back' })
}

</script>
